public class Vertex {
    private Integer num;
    private Integer weight;

    public Vertex(Integer num, Integer weight) {
        this.num = num;
        this.weight = weight;
    }

    public Integer getNum() {
        return num;
    }

    public Integer getWeight() {
        return weight;
    }
}
